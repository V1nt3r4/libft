#include "libft.h"

char *ft_strrchr(const char *s, int c)
{
    size_t i;
    i = ft_strlen(s);
    
    while (i >= 0 && s[i] != '\0')
    {
        if (s[i] == c)
            return ((char *)&s[i]);
        i--;
    }
    if(!'\0')
        return (0);
    
    return (0);
}